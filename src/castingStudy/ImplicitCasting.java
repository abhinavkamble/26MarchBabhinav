package castingStudy;

public class ImplicitCasting 
{

	public static void main(String[] args) 
	{
		// Converting lower data type information into higher data type information is called Implicit Casting
		// It is Also called as widening casting
		// Where memory size goes on increasing, there is no data loss
		
		int a=2220;
		System.out.println(a);
		
		long d=a;
		System.out.println(d);
		

	}

}
