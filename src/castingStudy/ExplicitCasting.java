package castingStudy;

public class ExplicitCasting 
{

	public static void main(String[] args) 
	{
		// Converting high data type information into lower data type information is called as Explicit Casting.
		// it is also called as Narrowing Casting
		//memory size is goes on decreasing,Data loss takes place
		
	    double a=2556656.2356;
	    System.out.println(a);
	    
	    int b=(int)a;
	    System.out.println(b);

	}

}
