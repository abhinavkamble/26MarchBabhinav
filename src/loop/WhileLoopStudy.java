package loop;

public class WhileLoopStudy {

	public static void main(String[] args) {
		
		//print abhinav name 5 times
		
		int a=11;
		while (a<=15)
		{
			System.out.println("abhinav");
			a++;
		}
		    System.out.println("This is My Name 5 Times");
		    System.out.println("======================");
		    
		//print 11's table in assending order
		
		int p=11;
		while (p<=110)
		{
			System.out.println(p);
			p=p+11;
		}
		    System.out.println("This is 11's Table in Assending Order");
		    System.out.println("=======================");
		    
		//print 11's table in descending order 
		    
		int x=110;
		while (x>=11)
		{
			System.out.println(x);
			x=x-15;
		}
		    System.out.println("This is 11's Table in descending Order");
		    System.out.println("=======================");
		    
	}

}
